const fs = require('fs');
const path = require('path');
const matter = require('gray-matter');

const projectsDir = path.join(__dirname, '../src/projects');
const outputFile = path.join(projectsDir, 'index.js');

// Get all MDX files
const mdxFiles = fs.readdirSync(projectsDir)
  .filter(file => file.endsWith('.mdx'))
  .sort(); // Sort for consistent ordering

// Generate projects data
const projectsData = mdxFiles.map(file => {
  const filePath = path.join(projectsDir, file);
  const content = fs.readFileSync(filePath, 'utf8');
  const { data: frontMatter } = matter(content);
  const id = file.replace('.mdx', '');

  return {
    id,
    ...frontMatter,
    component: `() => import('./${file}')`, // Dynamic import function
  };
});

// Generate the index.js file content
const fileContent = `// Auto-generated file - do not edit directly
// Generated by scripts/generate-projects-data.js

${mdxFiles.map(file => `import ${file.replace('.mdx', 'MDX')} from './${file}';`).join('\n')}

export const projectsData = [
${projectsData.map(project => `  {
    id: '${project.id}',
    title: ${JSON.stringify(project.title)},
    description: ${JSON.stringify(project.description)},
    github: ${JSON.stringify(project.github)},
    demo: ${JSON.stringify(project.demo)},
    tech: ${JSON.stringify(project.tech)},
    component: ${project.id}MDX,
  },`).join('\n')}
];
`;

// Write the file
fs.writeFileSync(outputFile, fileContent);
console.log(`Generated ${outputFile} with ${projectsData.length} projects`);
